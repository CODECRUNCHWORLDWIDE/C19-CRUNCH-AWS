# COMPARING THE TWO TEMPLATES
# ----------------------------------------------------------------------------
#
# The logical IDs differ (Data6E1F4D8F vs Data<other-hash>) because the hash is
# derived from the construct PATH, and the stack name differs (CrunchIacTsStack
# vs CrunchIacPyStack). But the RESOURCE TYPES and their PROPERTIES are equivalent.
#
# A quick structural comparison that ignores logical-ID names — count resource
# types in each template and confirm they match:
#
#   # requires jq
#   for f in ../crunch-iac-ts/cdk.out/CrunchIacTsStack.template.json \
#            cdk.out/CrunchIacPyStack.template.json; do
#     echo "== $f =="
#     jq -r '.Resources | to_entries[] | .value.Type' "$f" | sort | uniq -c | sort -rn
#   done
#
# Expected: both print the SAME counts per type, for example:
#
#       7 AWS::EC2::Subnet
#       4 AWS::EC2::RouteTable
#       ...
#       1 AWS::S3::Bucket
#       1 AWS::S3::BucketPolicy
#       1 AWS::KMS::Key
#       1 AWS::Lambda::Function
#       1 AWS::IAM::Role
#       1 AWS::IAM::Policy
#
# That identical type histogram is the proof of parity. The two languages are
# thin syntactic skins over the same jsii-generated construct library.
