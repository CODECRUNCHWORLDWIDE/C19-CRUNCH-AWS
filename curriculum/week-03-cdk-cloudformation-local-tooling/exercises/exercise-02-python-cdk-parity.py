# WHY THIS EXERCISE EXISTS
#
#   CDK's L2 constructs are generated from a single source (jsii) and projected
#   into every supported language. So the TypeScript Bucket and the Python Bucket
#   are the SAME construct with the SAME defaults — they MUST synthesize to
#   equivalent CloudFormation. Proving it to yourself kills the myth that "Python
#   CDK is different from TS CDK." It is the same library, different syntax.

from aws_cdk import (
    Stack,
    Duration,
    RemovalPolicy,
    CfnOutput,
)
from aws_cdk import aws_kms as kms
from aws_cdk import aws_s3 as s3
from aws_cdk import aws_ec2 as ec2
from aws_cdk import aws_lambda as lambda_
from constructs import Construct


class CrunchIacPyStack(Stack):
    def __init__(self, scope: Construct, construct_id: str, **kwargs) -> None:
        super().__init__(scope, construct_id, **kwargs)

        # --- VPC: two AZs, public + isolated subnets, NO NAT Gateway. ---
        vpc = ec2.Vpc(
            self,
            "Vpc",
            ip_addresses=ec2.IpAddresses.cidr("10.42.0.0/16"),
            max_azs=2,
            nat_gateways=0,  # deliberately zero — NAT is the silent budget killer
            subnet_configuration=[
                ec2.SubnetConfiguration(
                    name="public", subnet_type=ec2.SubnetType.PUBLIC, cidr_mask=24
                ),
                ec2.SubnetConfiguration(
                    name="isolated",
                    subnet_type=ec2.SubnetType.PRIVATE_ISOLATED,
                    cidr_mask=24,
                ),
            ],
        )

        # --- KMS customer-managed key with rotation. ---
        key = kms.Key(
            self,
            "DataKey",
            enable_key_rotation=True,
            description="CMK for the Crunch IaC starter data bucket",
            removal_policy=RemovalPolicy.DESTROY,  # dev only; RETAIN in prod
        )

        # --- S3 bucket: KMS-encrypted, versioned, locked down, lifecycle rules. ---
        bucket = s3.Bucket(
            self,
            "Data",
            encryption=s3.BucketEncryption.KMS,
            encryption_key=key,
            bucket_key_enabled=True,  # reduce KMS API calls / cost
            enforce_ssl=True,
            block_public_access=s3.BlockPublicAccess.BLOCK_ALL,
            versioned=True,
            removal_policy=RemovalPolicy.DESTROY,  # dev only; RETAIN in prod
            auto_delete_objects=True,  # dev only; lets `cdk destroy` empty the bucket
            lifecycle_rules=[
                s3.LifecycleRule(
                    id="tier-and-expire",
                    transitions=[
                        s3.Transition(
                            storage_class=s3.StorageClass.INFREQUENT_ACCESS,
                            transition_after=Duration.days(30),
                        )
                    ],
                    expiration=Duration.days(365),
                    noncurrent_version_expiration=Duration.days(90),
                )
            ],
        )

        # --- Lambda that reads from the bucket. ---
        reader = lambda_.Function(
            self,
            "Reader",
            runtime=lambda_.Runtime.PYTHON_3_12,
            handler="read_object.handler",
            code=lambda_.Code.from_asset("lambda"),
            timeout=Duration.seconds(15),
            environment={"BUCKET_NAME": bucket.bucket_name},
            vpc=vpc,
            vpc_subnets=ec2.SubnetSelection(
                subnet_type=ec2.SubnetType.PRIVATE_ISOLATED
            ),
        )

        # --- The one line that writes least-privilege IAM for you. ---
        # grant_read adds s3:GetObject/List* scoped to this bucket AND
        # kms:Decrypt on the key — identical to the TS grantRead.
        bucket.grant_read(reader)

        # --- Outputs. ---
        CfnOutput(self, "BucketName", value=bucket.bucket_name)
        CfnOutput(self, "KeyArn", value=key.key_arn)
        CfnOutput(self, "FunctionName", value=reader.function_name)


